package com.nith.nimbus2k22.modals;

public class TeamListModal {
    String name;
    String image;

    public TeamListModal(String name, String image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
